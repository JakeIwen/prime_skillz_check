// Write a switch statement that
// console logs witty statements for each
// day of the standard work week: Monday, Tuesday,
// Wednesday, Thursday, Friday
var dayOfWeek = '';

switch (dayOfWeek) {
  case 'Monday':
    console.log("Wish it were Sunday")
    break;
  case 'Tuesday':
    console.log("Kids eat free")
    break;
  case 'Wednesday':
    console.log("Hump day")
    break;
  case 'Thursday':
    console.log("I'm Thirsty")
    break;
  case 'Friday':
    console.log("That song that the pop music teenager made")
    break;
  default:
    console.log("It must be the weekend, dawg")
}
